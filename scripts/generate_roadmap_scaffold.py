def _extract_data_literal(html: str) -> str:
    """Return the `[...]` text of `const DATA = [ ... ];`, bracket-balanced."""
    m = re.search(r"const\s+DATA\s*=\s*\[", html)
    if not m:
        raise RuntimeError("Could not find `const DATA = [` in frontend/index.html")
    start = m.end() - 1
    depth, i, in_str, escaped = 0, start, None, False
    while i < len(html):
        ch = html[i]
        if in_str:
            if escaped:
                escaped = False
            elif ch == "\\":
                escaped = True
            elif ch == in_str:
                in_str = None
        else:
            if ch in ('"', "'"):
                in_str = ch
            elif ch == "[":
                depth += 1
            elif ch == "]":
                depth -= 1
                if depth == 0:
                    return html[start : i + 1]
        i += 1
    raise RuntimeError("Unterminated DATA array literal")


def _js_to_json(src: str) -> str:
    """Best-effort JS-object-literal → JSON normalizer.

    Tokenizes string-vs-code regions so key-quoting and trailing-comma stripping
    only touch code. Handles double-quoted strings as-is; rewrites single-quoted
    strings to JSON (defensive — current DATA uses only double quotes).
    """
    parts: list[tuple[bool, str]] = []  # (is_string, text)
    i, n = 0, len(src)
    while i < n:
        ch = src[i]
        if ch in ('"', "'"):
            quote = ch
            j = i + 1
            while j < n:
                if src[j] == "\\":
                    j += 2
                    continue
                if src[j] == quote:
                    break
                j += 1
            raw = src[i : j + 1]
            if quote == "'":
                inner = raw[1:-1].replace("\\'", "'").replace("\\\\", "\\")
                raw = json.dumps(inner, ensure_ascii=False)
            parts.append((True, raw))
            i = j + 1
        else:
            k = i
            while k < n and src[k] not in ('"', "'"):
                k += 1
            parts.append((False, src[i:k]))
            i = k

    out = []
    for is_str, text in parts:
        if is_str:
            out.append(text)
            continue
        text = re.sub(r'([{,]\s*)([A-Za-z_][A-Za-z0-9_]*)\s*:', r'\1"\2":', text)
        text = re.sub(r",(\s*[\]}])", r"\1", text)
        out.append(text)
    return "".join(out)


def _load_data(html: str) -> list[dict]:
    literal = _extract_data_literal(html)
    return json.loads(_js_to_json(literal))


def _week_description(week: dict) -> str:
    deliv = week.get("deliv") or []
    focus = week.get("focus") or []
    if deliv:
        joined = ". ".join(s.rstrip(".") for s in deliv if s)
        return joined + ("." if joined and not joined.endswith(".") else "")
    if focus:
        return focus[0]
    return ""


def _render_scaffold(data: list[dict]) -> str:
    lines = [
        START_MARK,
        '  <section data-roadmap-scaffold>',
        "    <h1>24-Week AI Generalist Roadmap</h1>",
        "    <p>Free, self-paced path from Python fundamentals to deployed AI applications. "
        "Six monthly phases, 24 weeks, curated resources from Stanford, MIT, "
        "Kaggle, Hugging Face, and leading practitioners. No signup, no paywall.</p>",
    ]
    week_count = 0
    for month in data:
        m_num = month.get("month", "?")
        m_label = escape(month.get("label", ""))
        m_title = escape(month.get("title", ""))
        tagline = escape(month.get("tagline", ""))
        lines.append(
            f"    <p><strong>Month {m_num} — {m_label}:</strong> {m_title}</p>"
        )
        if tagline:
            lines.append(f"    <p>{tagline}</p>")
        for week in month.get("weeks", []):
            n = week["n"]
            title = escape(week.get("t", ""))
            desc = escape(_week_description(week))
            resources = week.get("resources") or []
            items = "".join(
                f"        <li>{escape(r.get('name', ''))}</li>\n"
                for r in resources[:5]
                if r.get("name")
            )
            lines.append(f'    <section id="week-{n}">')
            lines.append(f"      <h2>Week {n}: {title}</h2>")
            lines.append(f"      <p>{desc}</p>")
            lines.append("      <ul>")
            # items already has trailing newlines; strip the final one for join
            lines.append(items.rstrip("\n"))
            lines.append("      </ul>")
            lines.append("    </section>")
            week_count += 1
    lines.append("  </section>")
    lines.append(f"  {END_MARK}")

    if week_count != 24:
        raise RuntimeError(f"Expected 24 weeks, scaffold built {week_count}")
    return "\n".join(lines)


def _flatten_weeks(data: list[dict]) -> list[dict]:
    weeks: list[dict] = []
    for month in data:
        for w in month.get("weeks", []):
            weeks.append(w)
    return weeks


