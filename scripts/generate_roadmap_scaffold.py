def _jsonld_safe(obj: dict) -> str:
    """Serialize to JSON-LD safe for embedding in a <script> element.

    Escapes `</` → `<\\/` (valid JSON per the spec's optional `\\/` escape) so
    no value can prematurely terminate the <script> element. indent=2 keeps
    regenerated diffs readable.
    """
    s = json.dumps(obj, ensure_ascii=False, indent=2)
    return s.replace("</", "<\\/")


def _validate_jsonld(course: dict, itemlist: dict, faq: dict) -> None:
    """Enforce SEO-05 acceptance constraints at generation time."""
    n_syllabus = len(course["syllabusSections"])
    if n_syllabus != 24:
        raise RuntimeError(f"Course.syllabusSections must have 24 entries, got {n_syllabus}")
    n_has_part = len(course["hasPart"])
    if n_has_part != 24:
        raise RuntimeError(f"Course.hasPart must have 24 entries, got {n_has_part}")
    n_items = len(itemlist["itemListElement"])
    if n_items != 24:
        raise RuntimeError(f"ItemList.itemListElement must have 24 entries, got {n_items}")
    for i, t in enumerate(course["teaches"]):
        if len(t) > 120:
            raise RuntimeError(f"teaches[{i}] is {len(t)} chars (>120): {t!r}")
    if len(course["coursePrerequisites"]) > 200:
        raise RuntimeError(
            f"coursePrerequisites is {len(course['coursePrerequisites'])} chars (>200)"
        )
    for i, q in enumerate(faq["mainEntity"]):
        words = len(q["acceptedAnswer"]["text"].split())
        if not (40 <= words <= 120):
            raise RuntimeError(
                f"FAQ[{i}] answer has {words} words, must be 40-120: {q['name']!r}"
            )


def _render_jsonld(data: list[dict]) -> str:
    course = _build_course(data)
    itemlist = _build_itemlist(data)
    faq = _build_faqpage()
    _validate_jsonld(course, itemlist, faq)
    blocks = [
        f'<script type="application/ld+json">\n{_jsonld_safe(course)}\n</script>',
        f'<script type="application/ld+json">\n{_jsonld_safe(itemlist)}\n</script>',
        f'<script type="application/ld+json">\n{_jsonld_safe(faq)}\n</script>',
    ]
    return "\n".join([JSONLD_START, *blocks, JSONLD_END])


def _inject_scaffold(html: str, scaffold: str) -> str:
    pattern = re.compile(
        re.escape(START_MARK) + r".*?" + re.escape(END_MARK),
        re.DOTALL,
    )
    if pattern.search(html):
        return pattern.sub(scaffold, html)
    if MAIN_EMPTY in html:
        return html.replace(MAIN_EMPTY, f"{MAIN_OPEN}\n{scaffold}\n</main>")
    # Fallback: inject inside an already-expanded <main id="content"> block
    return re.sub(
        r'(<main id="content">)(\s*)',
        lambda m: f"{m.group(1)}\n{scaffold}\n",
        html,
        count=1,
    )


def _inject_jsonld(html: str, block: str) -> str:
    pattern = re.compile(
        re.escape(JSONLD_START) + r".*?" + re.escape(JSONLD_END),
        re.DOTALL,
    )
    if pattern.search(html):
        return pattern.sub(block, html)
    # First run: insert before </head>. Match only the first (real) </head> —
    # later `</head>` strings live inside JS-string HTML templates for PDF
    # export and must not be touched.
    return html.replace("</head>", f"{block}\n</head>", 1)


def _inject(html: str, scaffold: str, jsonld: str) -> str:
    html = _inject_scaffold(html, scaffold)
    html = _inject_jsonld(html, jsonld)
    return html


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__.strip().splitlines()[0])
    ap.add_argument(
        "--check",
        action="store_true",
        help="Exit 1 if the scaffold on disk is out of sync with DATA",
    )
    args = ap.parse_args()

    html = FRONTEND.read_text(encoding="utf-8")
    data = _load_data(html)
    scaffold = _render_scaffold(data)
    jsonld = _render_jsonld(data)
    new_html = _inject(html, scaffold, jsonld)

    if args.check:
        if new_html != html:
            print(
                "Scaffold/JSON-LD drift detected. Run: python scripts/generate_roadmap_scaffold.py",
                file=sys.stderr,
            )
            return 1
        print("Scaffold + JSON-LD up to date (24 weeks).")
        return 0

    FRONTEND.write_text(new_html, encoding="utf-8")
    print(
        f"Regenerated: scaffold {len(scaffold)} bytes, JSON-LD {len(jsonld)} bytes "
        f"(24 weeks, 12 FAQs, 8 teaches)."
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
